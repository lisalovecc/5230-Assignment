# 0.5 DATA LOADING (IMDB MOVIE REVIEWS - BALANCED)
# ==============================================================================
def load_imdb_data(max_tokens=MAX_TOKENS):
    print(f"Loading IMDB dataset (Limited to {max_tokens} tokens for speed)...")
    dataset = load_dataset("imdb")

    # Extract balanced text (Half negative, half positive)
    train_reviews = dataset['train']['text'][:250] + dataset['train']['text'][-250:]
    val_reviews = dataset['test']['text'][:50] + dataset['test']['text'][-50:]

    # Shuffle the reviews to mix positive and negative
    random.seed(42)
    random.shuffle(train_reviews)
    random.shuffle(val_reviews)

    def clean_text(text):
        text = text.lower()
        text = re.sub(r'<br\s*/>', ' ', text)
        return re.sub(r'[^a-z0-9\s]', '', text).split()

    train_words = []
    for review in train_reviews:
        train_words.extend(clean_text(review))
    train_words = train_words[:max_tokens]

    val_words = []
    for review in val_reviews:
        val_words.extend(clean_text(review))
    val_words = val_words[:max_tokens // 5]

    # Build Vocabulary
    counter = Counter(train_words)
    vocab = {word: i+2 for i, word in enumerate(counter.keys())}
    vocab['<unk>'] = 0
    vocab['<pad>'] = 1
    idx_to_word = {i: word for word, i in vocab.items()}

    # Encode for PyTorch models
    def encode_lm(text_list):
        tokens = [vocab.get(word, 0) for word in text_list]
        return torch.tensor(tokens, dtype=torch.long)

    train_data_lm = encode_lm(train_words)
    val_data_lm = encode_lm(val_words)

    def batchify(data, bsz):
        seq_len_actual = data.size(0) // bsz
        data = data[:seq_len_actual * bsz]
        return data.view(bsz, seq_len_actual).t().contiguous().to(DEVICE)

    train_data_lm = batchify(train_data_lm, BATCH_SIZE)
    val_data_lm = batchify(val_data_lm, BATCH_SIZE)

    return train_data_lm, val_data_lm, train_words, val_words, vocab, idx_to_word

def get_lm_batch(source, i, seq_len=SEQ_LEN):
    seq_len_actual = min(seq_len, len(source) - 1 - i)
    data = source[i:i+seq_len_actual]
    target = source[i+1:i+1+seq_len_actual].reshape(-1)
    return data, target

