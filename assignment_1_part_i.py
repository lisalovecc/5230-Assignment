class NGramLM:
    """A simple N-Gram language model using Maximum Likelihood Estimation."""
    def __init__(self, n=3):
        self.n = n
        self.ngram_counts = defaultdict(int)
        self.context_counts = defaultdict(int)
        self.vocab = set()
        self.vocab_size = 0

    def train(self, tokenized_corpus):
        for i in range(len(tokenized_corpus) - self.n + 1):
            ngram = tuple(tokenized_corpus[i:i+self.n])
            context = tuple(tokenized_corpus[i:i+self.n-1])
            self.ngram_counts[ngram] += 1
            self.context_counts[context] += 1
            self.vocab.add(tokenized_corpus[i])
        self.vocab_size = len(self.vocab)

    def evaluate(self, tokenized_corpus):
        """Calculates Perplexity for the N-gram model."""
        log_prob_sum = 0
        for i in range(len(tokenized_corpus) - self.n + 1):
            ngram = tuple(tokenized_corpus[i:i+self.n])
            context = tuple(tokenized_corpus[i:i+self.n-1])
            count = self.ngram_counts.get(ngram, 0)
            ctx_count = self.context_counts.get(context, 0)
            # Add-1 Smoothing
            prob = (count + 1) / (ctx_count + self.vocab_size)
            log_prob_sum += math.log(prob)
        avg_log_prob = log_prob_sum / (len(tokenized_corpus) - self.n + 1)
        return math.exp(-avg_log_prob)

    def generate(self, start_context, max_len=20):
        current_context = tuple(start_context[-(self.n-1):])
        generated = list(start_context)
        for _ in range(max_len):
            best_word, best_prob = None, -1
            for word in self.vocab:
                ngram = current_context + (word,)
                count = self.ngram_counts.get(ngram, 0)
                ctx_count = self.context_counts.get(current_context, 0)
                prob = (count + 1) / (ctx_count + self.vocab_size)
                if prob > best_prob:
                    best_prob, best_word = prob, word
            generated.append(best_word)
            current_context = tuple(generated[-(self.n-1):])
        return " ".join(generated)

class NeuralLMBase(nn.Module):
    """Base neural module"""
    def __init__(self, vocab_size, embed_size):
        super().__init__()
        self.embedding = nn.Embedding(vocab_size, embed_size)

class RNNModel(NeuralLMBase):
    def __init__(self, vocab_size, embed_size, hidden_size):
        super().__init__(vocab_size, embed_size)
        self.rnn = nn.RNN(embed_size, hidden_size, batch_first=True)
        self.fc = nn.Linear(hidden_size, vocab_size)

    def forward(self, x, hidden=None):
        emb = self.embedding(x)
        out, hidden = self.rnn(emb, hidden)
        logits = self.fc(out)
        return logits, hidden

class LSTMModel(NeuralLMBase):
    def __init__(self, vocab_size, embed_size, hidden_size):
        super().__init__(vocab_size, embed_size)
        self.lstm = nn.LSTM(embed_size, hidden_size, batch_first=True)
        self.fc = nn.Linear(hidden_size, vocab_size)

    def forward(self, x, hidden=None):
        emb = self.embedding(x)
        out, hidden = self.lstm(emb, hidden)
        logits = self.fc(out)
        return logits, hidden

class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_len=5000):
        super().__init__()
        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len, dtype=torch.float).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
        pe[:, 0::2] = torch.sin(position * div_term)
        pe[:, 1::2] = torch.cos(position * div_term)
        self.register_buffer('pe', pe.unsqueeze(0))

    def forward(self, x):
        x = x + self.pe[:, :x.size(1)]
        return x

class TransformerLM(NeuralLMBase):
    def __init__(self, vocab_size, embed_size, num_heads=4, hidden_size=256, num_layers=2):
        super().__init__(vocab_size, embed_size)
        self.pos_encoder = PositionalEncoding(embed_size)
        encoder_layers = nn.TransformerEncoderLayer(embed_size, num_heads, hidden_size, batch_first=True)
        self.transformer_encoder = nn.TransformerEncoder(encoder_layers, num_layers)
        self.fc = nn.Linear(embed_size, vocab_size)
        self.embed_size = embed_size

    def generate_square_subsequent_mask(self, sz):
        mask = (torch.triu(torch.ones(sz, sz)) == 1).transpose(0, 1)
        mask = mask.float().masked_fill(mask == 0, float('-inf')).masked_fill(mask == 1, float(0.0))
        return mask.to(DEVICE)

    def forward(self, x, hidden=None):
        src_mask = self.generate_square_subsequent_mask(x.size(1))
        emb = self.embedding(x) * math.sqrt(self.embed_size)
        emb = self.pos_encoder(emb)
        out = self.transformer_encoder(emb, src_mask)
        logits = self.fc(out)
        return logits, None

