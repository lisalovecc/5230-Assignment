DEVICE = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
EMBED_SIZE = 128
HIDDEN_SIZE = 256
BATCH_SIZE = 32
SEQ_LEN = 35
EPOCHS = 20
LEARNING_RATE = 0.001
MAX_TOKENS = 100000
