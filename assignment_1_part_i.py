def train_lm(model, data_source, criterion, optimizer):
    model.train()
    total_loss = 0.
    start_time = time.time()
    for i in range(0, data_source.size(0) - 1, SEQ_LEN):
        inputs, targets = get_lm_batch(data_source, i)
        optimizer.zero_grad()
        output, _ = model(inputs)
        loss = criterion(output.view(-1, output.size(-1)), targets)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 5.0)
        optimizer.step()
        total_loss += loss.item()
    batches = max(1, (data_source.size(0) - 1) // SEQ_LEN)
    return total_loss / batches, time.time() - start_time

def evaluate_lm(model, data_source, criterion):
    model.eval()
    total_loss = 0.
    start_time = time.time()
    with torch.no_grad():
        for i in range(0, data_source.size(0) - 1, SEQ_LEN):
            inputs, targets = get_lm_batch(data_source, i)
            output, _ = model(inputs)
            loss = criterion(output.view(-1, output.size(-1)), targets)
            total_loss += loss.item()
    batches = max(1, (data_source.size(0) - 1) // SEQ_LEN)
    inference_time_per_batch = (time.time() - start_time) / batches
    return total_loss / batches, inference_time_per_batch

def generate_neural_text(model, vocab, i2w, start_sequence, max_len=15):
    """Generates a sequence of text to test the model's qualitative performance."""
    model.eval()
    words = start_sequence.lower().split()
    input_ids = torch.tensor([[vocab.get(w, 0) for w in words]]).to(DEVICE)
    with torch.no_grad():
        for _ in range(max_len):
            output, _ = model(input_ids)
            last_word_logits = output[0, -1, :]
            predicted_id = torch.argmax(last_word_logits).item()
            predicted_word = i2w.get(predicted_id, '<unk>')
            words.append(predicted_word)
            input_ids = torch.cat([input_ids, torch.tensor([[predicted_id]]).to(DEVICE)], dim=1)
    return " ".join(words)

